# 1. 输入一个整数，代表树干的高度.
#   打印一棵"圣诞树"
#   如:
#    输入:2
#   打印
#     *
#    ***
#     *
#     *
#    输入:3
#   打印
#      *
#     ***
#    *****
#      *
#      *    
#      *

n = int(input("请输入树干高度: "))

# 打印树叶部分

for i in range(1, n + 1):  # i代表从上向下的行号
    blanks_count = n - i  # 计算空格个数
    print(' '* blanks_count + '*' * (2*i-1))

# 打印树十部分
for i in range(1, n + 1):
    print(' ' * (n - 1) + '*')






